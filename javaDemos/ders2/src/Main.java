public class Main {

    public static void main(String[] args) {

        // For
        for(int i=0;i<10;i++){
            System.out.println(i);
        }
        System.out.println("Döngü bitti..");

        // While
        int j=2;
        while (j<10){
            System.out.println(j);
            j++;
        }
        System.out.println("Döngü bitti..");

        //Do while
        int k=3;
        do{
            System.out.println(k);
            k++;
        }while (k<10);
        System.out.println("Döngü bitti..");
    }
}
